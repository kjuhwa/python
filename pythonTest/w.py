# for 변수 in 복합데이터티입:
st = 'abcd'
for s in st:
    print(s)
myList = [10,20,30,40]
for n in myList:
    print(n)
t = (1,2,3)
for n in t:
    print(n)
d={'aa':10,'bb':20,'cc':30}
for n in d:
    print(n)
for n in d.keys():
    print(n)
for n in d.values():
    print(n)
for n in d.items():
    print(n)
for k, v in d.items():
    print(k, v)
